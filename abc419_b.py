import heapq
q = int(input())
B = []  
for _ in range(q):
    qr = list(map(int, input().split()))
    if qr[0] == 1:
        x = qr[1]
        heapq.heappush(B, x)  
    elif qr[0] == 2:
        m = heapq.heappop(B)   
        print(m)
