from atcoder.scc import SCCGraph


def solve(N, A):
    G = SCCGraph(N)
    for x, a in enumerate(A):
        G.add_edge(x, a)

    ans = 0
    for group in G.scc():
        if (s := len(group)) > 1:
            ans += s
        else:
            x = group[0]
            ans += int(x == A[x])

    return ans


def main():
    N = int(input())
    A = list(map(int, input().split()))
    A = [a - 1 for a in A]
    ans = solve(N, A)
    print(ans)


if __name__ == "__main__":
    main()

