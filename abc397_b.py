import sys
input = sys.stdin.readline

S=list(input().strip())
ANS=0

while S:
    if len(S)>=2:
        if S[0]=="i" and S[1]=="o":
            S=S[2:]
            continue
        if S[0]=="i" and S[1]=="i":
            ANS+=1
            S.pop(0)
            continue
        if S[0]=="o" and S[1]=="o":
            ANS+=1
            S.pop(0)
            continue
        if S[0]=="o" and S[1]=="i":
            ANS+=1
            S.pop(0)
            continue
    if len(S)==1:
        ANS+=1
        S.pop(0)

print(ANS)

    
