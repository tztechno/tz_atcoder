# AtCoder社の入口には特殊なパスコード入力装置があり、この装置は文字列を1つ表示する画面と2つのボタンからなります。画面に表示される文字列をtとします。tははじめ空文字列であり、ボタンを押すことでtに以下の変化が起きます。ボタンAを押すと、tの末尾に0が追加される。ボタンBを押すと、tに含まれるすべての数字が、それぞれ次の数字に置き換わる。ここで、0から8までの数字については次の数字は1大きな数を表す数字とし、9の次の数字は0とする。たとえば、tが1984のときにボタンAを押すとtは19840に変化し、さらにボタンBを押すとtは20951に変化します。文字列Sが与えられます。tが空文字列である状態からボタンを0回以上押してtをSに一致させるとき、ボタンを最少で何回押す必要があるかを求めてください。

