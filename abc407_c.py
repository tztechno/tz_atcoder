import sys
input = sys.stdin.readline
S=input().strip()
now=0
ANS=0

for s in S[::-1]:
    ANS+=1
    x=int(s)
    k=(x-now)%10 ####
    ANS+=k
    now=(now+k)%10 ####

print(ANS)

