T = list(map(int, input()))
t = 0
while T:
    zero_count = 0
    while T and T[-1] == 0:
        T.pop()
        zero_count += 1
    t += zero_count
    if not T:
        break
    for i in range(len(T)):
        T[i] = (T[i] - 1) % 10
    t += 1
print(t)
