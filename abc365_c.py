N,M=map(int,input().split())
A=list(map(int,input().split()))
A.sort()
B=[0]
for a in A:
  B+=[B[-1]+a]
from bisect import bisect_left,bisect_right,bisect
amin=min(A)
amax=max(A)
for s in range(amin,amax+1):
  bl=bisect_left(A,s)
  T=B[bl]+s*(N-bl)
  if T>M:
    print(s-1)
    exit()
print('infinite')

