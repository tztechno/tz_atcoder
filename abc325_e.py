import heapq
n,a,b,c=map(int,input().split())
d=[list(map(int,input().split())) for i in range(n)]
que=[]
heapq.heapify(que)
INF=1001001001001001001
dist=[INF]*n
dist[0]=0
heapq.heappush(que,(dist[0],0))
while que:
  dst,v=heapq.heappop(que)
  for nv in range(n):
    if v==nv:continue
    if dist[nv]>dst+d[v][nv]*a:
      dist[nv]=dst+d[v][nv]*a
      heapq.heappush(que,(dist[nv],nv))
for i in range(n):
  heapq.heappush(que,(dist[i],i))
  while que:
    dst,v=heapq.heappop(que)
    for nv in range(n):
      if v==nv:continue
      if dist[nv]>dst+d[v][nv]*b+c:
        dist[nv]=dst+d[v][nv]*b+c
        heapq.heappush(que,(dist[nv],nv))
print(dist[-1])    
