n=int(input())
#ab+cd=n
AB=[]
C=[]
for ab in range(1,n):
  for a in range(1,n):
    if ab%a==0:
      b=ab//a
      AB+=[(a,b,ab)]
      C+=[ab]
t=0
ANS=[]
for abi in AB:
  a,b,ab =abi
  cd=n-ab
  if cd in C:
    for c in range(1,n):
      if cd%c==0:
        d=cd//c
        ANS+=[(a,b,c,d)]
        t+=1
print(t)

