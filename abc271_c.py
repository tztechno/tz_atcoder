from sortedcontainers import SortedSet
N=int(input())
A=list(map(int,input().split()))
B=SortedSet()
rem=0
for a in A:
    if a in B:rem+=1
    else:B.add(a)
ans=1
while 1:
    if ans in B:
        B.pop(0)
        ans+=1
    else:
        if rem>=2:
            rem-=2
            ans+=1
        elif rem==1:
            rem=0
            if len(B)>0:
                B.pop(-1)
                ans+=1
            else:
                break
        elif len(B)>=2:
            B.pop(-1)
            B.pop(-1)
            ans+=1
        else:
            break
print(ans-1)

