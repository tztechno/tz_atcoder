N=int(input())
D=[]
for i in range(1,N):
  D+=[(i**3,i)]
for x in range(N):
  for y in range(x):
    if D[x][0]-D[y][0]==N:
      print(D[x][1],D[y][1])
      exit()
else:
  print(-1)
