x, a, d, n = map(int, input().split())
ans = abs(a - x)
left = 0
right = n

while right > left + 2:
    mid1 = (2 * left + right) // 3
    mid2 = (left + 2 * right) // 3
    score1 = abs(a + d * mid1 - x)
    score2 = abs(a + d * mid2 - x)
    if score1 > score2:
        left = mid1
    else:
        right = mid2

for k in range(left, min(right + 1, n + 1)):
    ans = min(ans, abs(a + d * k - x))

print(ans)
