def rotate_matrix(matrix):
    # 転置して各行を反転させることで時計回りに90度回転させる
    return [list(reversed(row)) for row in zip(*matrix)]

# 使用例
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

rotated_matrix = rotate_matrix(matrix)
for row in rotated_matrix:
    print(row)

