A=list(map(int,input().split()))
ANS=[0]*200
for a in A:
    MIN=min(a,199)
    for i in range(MIN,-1,-1):
        ANS[i]+=1
score=0
for i in range(len(ANS)):
    if ANS[i]>=i:
        score=i
print(score)
