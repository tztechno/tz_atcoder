import sys
input = sys.stdin.readline

N,X,Y=map(int,input().split())
A=list(map(int,input().split()))

A.sort()

MAX=Y*A[0]
k=Y-X
ANS=0

for a in A:
    MIN=X*a
    YS=(MAX-MIN)//k
    XS=a-YS

    if XS>=0 and YS>=0 and XS*X+YS*Y==MAX:
        ANS+=YS
    else:
        print(-1)
        exit()
