import sys
input = sys.stdin.readline

N,R,C=map(int,input().split())
S=input().strip()

NOW={(0,0)}
x=y=0
ANS=[]

for s in S:
    if s=="N":
        R+=1
        x+=1
    elif s=="S":
        R-=1
        x-=1
    elif s=="E":
        C-=1
        y-=1
    else:
        C+=1
        y+=1
    NOW.add((x,y))

    #print((R,C),NOW)

    if (R,C) in NOW:
        ANS.append(1)
    else:
        ANS.append(0)

print("".join(map(str,ANS)))
