N, R, C = map(int, input().split())
S = list(input())
wind = {
  "N": (-1, 0),
  "W": (0, -1),
  "S": (1, 0),
  "E": (0, 1)
}

p = set()
p.add((0, 0))
wx, wy = 0, 0
ans = []

for i in range(N):
  dx, dy = wind[S[i]]
  wx += dx
  wy += dy
  
  target_vec = (wx - R, wy - C)
  
  if target_vec in p:
    ans.append("1")
  else:
    ans.append("0")
    
  p.add((wx, wy))
  
print("".join(ans))
