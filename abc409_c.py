import sys
input = sys.stdin.readline
N,L=map(int,input().split())
A=list(map(int,input().split()))

X=[0]*L
X[0]=1
now=0
for a in A:
    now+=a
    now%=L
    X[now]+=1
ANS=0
if L%3==0:
    for i in range(L//3):
        ANS+=X[i]*X[i+L//3]*X[i+L//3*2]
print(ANS)
