import sys
input = sys.stdin.readline

N,M=map(int,input().split())

SET=set()

for i in range(M):
    a,b=map(int,input().split())
    SET.add((a,b))

    for x,y in [(2,1),(2,-1),(-2,1),(-2,-1),(1,2),(1,-2),(-1,2),(-1,-2)]:
        if 1<=a+x<=N and 1<=b+y<=N:
            SET.add((a+x,b+y))

print(N*N-len(SET))
