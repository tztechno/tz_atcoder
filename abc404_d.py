import sys
input = sys.stdin.readline

N,M=map(int,input().split())
C=list(map(int,input().split()))

LIST=[[] for i in range(N)]

for i in range(M):
    X=list(map(int,input().split()))

    for x in X[1:]:
        LIST[x-1].append(i)

LIST+=LIST
C+=C

ANS=1<<63

for i in range(1<<(N*2)):
    money=0
    MS=[0]*M

    for j in range(2*N):
        if i & (1<<j) != 0:
            money+=C[j]
            for x in LIST[j]:
                MS[x]+=1

    if min(MS)>=2:
        ANS=min(ANS,money)

print(ANS)
                

